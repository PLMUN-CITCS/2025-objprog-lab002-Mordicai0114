public class VariablesAndCaseSensitivity {
    public static void main(String[] args) {

        int studentAge = 15;
        double itemPrice = 29.99;
        String studentName = "Alice";
        int counter = 10;
        int counterDifferentCase = 20; 
    
        System.out.println("Student's age is: " + studentAge);
        System.out.println("Item's price is: " + itemPrice);
        System.out.println("Student's name is: " + studentName); 
        System.out.println("Counter (lowercase): " + counter);
        System.out.println("Counter (Different case): " + counterDifferentCase); 
    }
}